package com.trycatch_tanmay.naamkaran;

public class NameItem {
    private String name;
    private String meaning;

    public NameItem(String name, String meaning) {
        this.name = name;
        this.meaning = meaning;
    }

    public String getName() {
        return name;
    }

    public String getMeaning() {
        return meaning;
    }
}
